#!/usr/bin/env python3
'''
Este módulo contiene una función para sumar dos números flotantes.
'''
def add(a: float, b: float) -> float:
    """
    Suma dos números flotantes y devuelve su suma.

    Args:
        a (float): El primer número flotante.
        b (float): El segundo número flotante.

    Returns:
        float: La suma de a y b.
    """
    return a + b
